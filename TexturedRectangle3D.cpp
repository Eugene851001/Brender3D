#include "TexturedRectangle3D.h"
#include <SOIL.h>
#include <iostream>

TexturedRectangle3D::TexturedRectangle3D(glm::vec3 position, float length, float height, float width, Texture *texture) :
	Rectangle3D(position, length, height, width)  
{
    rectType = rect::RECT_TEXTURED;
    this->texture = texture;

}

float* TexturedRectangle3D::getVertexArray(int& size)
{
    GLfloat verticesArray[] = {
        -0.5f, -0.5f, -0.5f,  0.0f, 0.0f,
         0.5f, -0.5f, -0.5f,  1.0f, 0.0f,
         0.5f,  0.5f, -0.5f,  1.0f, 1.0f,
         0.5f,  0.5f, -0.5f,  1.0f, 1.0f,
        -0.5f,  0.5f, -0.5f,  0.0f, 1.0f,
        -0.5f, -0.5f, -0.5f,  0.0f, 0.0f,

        -0.5f, -0.5f,  0.5f,  0.0f, 0.0f,
         0.5f, -0.5f,  0.5f,  1.0f, 0.0f,
         0.5f,  0.5f,  0.5f,  1.0f, 1.0f,
         0.5f,  0.5f,  0.5f,  1.0f, 1.0f,
        -0.5f,  0.5f,  0.5f,  0.0f, 1.0f,
        -0.5f, -0.5f,  0.5f,  0.0f, 0.0f,

        -0.5f,  0.5f,  0.5f,  1.0f, 0.0f,
        -0.5f,  0.5f, -0.5f,  1.0f, 1.0f,
        -0.5f, -0.5f, -0.5f,  0.0f, 1.0f,
        -0.5f, -0.5f, -0.5f,  0.0f, 1.0f,
        -0.5f, -0.5f,  0.5f,  0.0f, 0.0f,
        -0.5f,  0.5f,  0.5f,  1.0f, 0.0f,

         0.5f,  0.5f,  0.5f,  1.0f, 0.0f,
         0.5f,  0.5f, -0.5f,  1.0f, 1.0f,
         0.5f, -0.5f, -0.5f,  0.0f, 1.0f,
         0.5f, -0.5f, -0.5f,  0.0f, 1.0f,
         0.5f, -0.5f,  0.5f,  0.0f, 0.0f,
         0.5f,  0.5f,  0.5f,  1.0f, 0.0f,

        -0.5f, -0.5f, -0.5f,  0.0f, 1.0f,
         0.5f, -0.5f, -0.5f,  1.0f, 1.0f,
         0.5f, -0.5f,  0.5f,  1.0f, 0.0f,
         0.5f, -0.5f,  0.5f,  1.0f, 0.0f,
        -0.5f, -0.5f,  0.5f,  0.0f, 0.0f,
        -0.5f, -0.5f, -0.5f,  0.0f, 1.0f,

        -0.5f,  0.5f, -0.5f,  0.0f, 1.0f,
         0.5f,  0.5f, -0.5f,  1.0f, 1.0f,
         0.5f,  0.5f,  0.5f,  1.0f, 0.0f,
         0.5f,  0.5f,  0.5f,  1.0f, 0.0f,
        -0.5f,  0.5f,  0.5f,  0.0f, 0.0f,
        -0.5f,  0.5f, -0.5f,  0.0f, 1.0f
    };
    size = sizeof(verticesArray);
    if (vertices != NULL)
    {
        return vertices;
    }
    int amount = sizeof(verticesArray) / sizeof(float);
    vertices = new float[amount];
    for (int i = 0; i < amount; i++)
    {
        vertices[i] = verticesArray[i];
    }
    for (int i = 0; i < 6 * 6; i++)
    {
        if (vertices[5 * i] == -0.5)
            vertices[5 * i] = 0;
        else
            vertices[5 * i] = length;

        if (vertices[5 * i + 1] == -0.5)
            vertices[5 * i + 1] = -height;
        else
            vertices[5 * i + 1] = 0;

        if (vertices[5 * i + 2] == -0.5)
            vertices[5 * i + 2] = 0 - width;
        else
            vertices[5 * i + 2] = 0;
    }
    return vertices;
}

GLuint TexturedRectangle3D::getVAO()
{
    if (VAO != -1)
        return VAO;
    GLuint VBO, VAO;
    int size;
    getVertexArray(size);
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);

    glBindVertexArray(VAO);

    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, size, vertices, GL_DYNAMIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(GLfloat), (GLvoid*)0);
    glEnableVertexAttribArray(0);

    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(GLfloat), (GLvoid*)(3 * sizeof(GLfloat)));
    glEnableVertexAttribArray(1);

    glBindBuffer(GL_ARRAY_BUFFER, 0);

    glBindVertexArray(0);
    this->VAO = VAO;
    this->VBO = VBO;
    return VAO;
}

GLuint TexturedRectangle3D::getTextureId()
{
    return texture->getId();
}

